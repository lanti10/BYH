export function translate(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>
): string {
  let s = dict[locale]?.[key] ?? dict[DEFAULT_LOCALE][key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) s = s.replaceAll(`{${k}}`, String(v));
  }
  return s;
}

